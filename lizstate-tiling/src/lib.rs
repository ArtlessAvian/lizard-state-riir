//! Square tilings that fill 2D spaces (plural!).
//! Or, 2D spaces formed by joining square tiles.
//!
//! Also `no_std`!
#![no_std]
//! (Maybe)
//!
//! All Copy + Eq traits are intended to be implemented on references or unit types.
//! All Clone + Eq traits are intended to be implemented on the previous OR smart pointers.

#![allow(dead_code)]
#![warn(clippy::pedantic)]

/// A trait for 4-regular undirected graphs.
///
/// Every *directed* edges is labeled, and their reverse edges are labeled their inverse label.
pub mod tiling;

/// Sequences of directions.
///
/// A trait for behavior, and implementors with finite storage.
pub mod path;

/// A trait for Groups generated by `Up` and `Right`.
///
/// Can be seen as edge supergraphs of the 4-regular undirected graph.
pub mod group;

/// The familiar 2D coordinates, implementing `HasSquareTiling`.
///
/// Also known as the group Z \cross Z. A free abelian group!
pub mod euclidean_plane;

/// A wrapper to make Paths behave like a group!
///
/// Intercepts the `PathLike` trait to cancel redundant movement.
pub mod free_group;

/// Runtime defined equality classes on paths.
///
/// Appending paths preserves equality.
/// Prepending paths does NOT preserve equality! (Not a group!)
pub mod path_equality;
